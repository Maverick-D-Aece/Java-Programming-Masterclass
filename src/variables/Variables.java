package variables;

/**
 * Shows a bunch of primitive variables that we can have in Java
 * and how we can print them.
 * Version 1.1 shows how we can assign a new value to a variable as an expression.
 * @author Maverick D. Aece
 * @version 1.1
 */
public class Variables {

    public static void main(String[] args) {
        // declares and defines(initializes) an integer variable
        int number = 7 + 5;
        System.out.println("Value of the \"number\" variable = " + number);
    }
}
